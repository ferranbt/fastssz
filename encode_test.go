func ExtendUint[T constraints.Integer](b []T, needLen int) []T {
	if b == nil {
		b = []T{}
	}
	b = b[:cap(b)]
	if n := needLen - cap(b); n > 0 {
		b = append(b, make([]T, n)...)
	}
	return b[:needLen]
}
